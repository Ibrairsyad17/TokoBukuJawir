    private void kPembayaranActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kPembayaranActionPerformed
        // TODO add your handling code here:
        String kodePesan = kodPes.getText();
        String rekeningB = rekBank.getText();
        if(rekeningB.isEmpty() || kodePesan.isEmpty()){
            JOptionPane.showMessageDialog(this, "Pilih buku dan metode pembayaran", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            try{
                Class.forName("com.mysql.jdbc.Driver");
                Connection cnc = DriverManager.getConnection("jdbc:mysql://localhost:3306/uas", "root", "");
                String cmd_sql = "UPDATE pesanan SET status = \"Telah dibayar\" WHERE id_pesanan = ?";
                PreparedStatement stm = cnc.prepareStatement(cmd_sql);
                stm.setString(1, kodePesan);
                
                int rslt = stm.executeUpdate();
                
                
                if(rslt > 0){
                    JOptionPane.showMessageDialog(this, "Berhasil di merubah jumlah dari keranjang", "Berhasil", JOptionPane.INFORMATION_MESSAGE);
                    model.setRowCount(0);
                    
                    tampilPesanan(nom);
                } else {
                    JOptionPane.showMessageDialog(null, "Tidak valid!");
                    kodPes.setText("");
                    rekBank.setText("");
                }
                cnc.close();
                
            } catch (Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_kPembayaranActionPerformed

