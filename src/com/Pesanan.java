            String cmd_sql = "SELECT id_pesanan, buku.nama_buku, status, harga_pesanan FROM pesanan INNER JOIN buku ON pesanan.id_buku = buku.id_buku WHERE harga_pesanan = (SELECT MIN(harga_pesanan) FROM pesanan);";
